async function fillEbayListing() {
  try {
    const stored = await browser.storage.local.get([
      "latestDiscogsRelease",
      "autofillDescription"
    ]);

    const release = stored.latestDiscogsRelease;

    if (!release) {
      throw new Error(
        "No Discogs release is saved. Generate a title on Discogs first."
      );
    }

    if (
      typeof release.savedAt === "number" &&
      Date.now() - release.savedAt > MAX_STORED_DATA_AGE_MS
    ) {
      throw new Error(
        "The saved Discogs data is over 12 hours old. Generate it again first."
      );
    }

    // Backward-safe default for data generated by an earlier version.
    release.format = cleanText(release.format) || DEFAULT_FORMAT;

    // Ensure LP is present before the suffix even when old saved metadata
    // came from a previous extension version.
    release.ebayTitle = ensureFormatInTitle(
      release.ebayTitle,
      release.format
    );

    const titleField = findTitleField(document);

    if (!titleField) {
      throw new Error(
        "Could not find the eBay listing title field on this page."
      );
    }

    setEditableValue(titleField, release.ebayTitle);

    const descriptionEnabled =
      stored.autofillDescription !== false;

    let descriptionResult = {
      foundEditor: false,
      updatedFields: [],
      missingFields: []
    };

    if (descriptionEnabled) {
      descriptionResult = fillDescriptionAcrossDocuments(release);
    }

    if (descriptionEnabled && descriptionResult.editor) {
      scrollToDescriptionEditor(descriptionResult.editor);
    } else {
      titleField.scrollIntoView({
        behavior: "smooth",
        block: "center"
      });
    }

    let detailMessage;

    if (!descriptionEnabled) {
      detailMessage = " Description autofill is disabled in settings.";
    } else if (descriptionResult.updatedFields.length) {
      detailMessage =
        ` Description fields updated: ` +
        `${descriptionResult.updatedFields.join(", ")}.`;
    } else {
      detailMessage =
        " The title was filled, but no matching description labels were found.";
    }

    showToast(
      `eBay title filled.${detailMessage} Review the draft before saving.`,
      descriptionResult.updatedFields.length || !descriptionEnabled
        ? "success"
        : "warning"
    );

    console.log("eBay fill result:", {
      title: release.ebayTitle,
      descriptionResult
    });
  } catch (error) {
    showToast(error.message || "Unable to fill the eBay listing.", "error");
    console.error(error);
  }
}

function ensureFormatInTitle(title, format) {
  const value = cleanText(title);
  const safeFormat = cleanText(format) || DEFAULT_FORMAT;

  if (!value) {
    return value;
  }

  // Current titles already contain LP/2xLP before the optional suffix.
  if (new RegExp(`(?:^| - )${escapeRegExp(safeFormat)}(?: - |$)`, "i").test(value)) {
    return value;
  }

  // Known configured suffixes appear at the end. Insert the format
  // immediately before the suffix so the structure remains:
  // Cat No. - LP - Suffix
  const suffixPatterns = [
    /\s+-\s+JAZZ\s+-\s+GREAT COPY!$/i,
    /\s+-\s+SOUL\s+-\s+GREAT COPY!$/i,
    /\s+-\s+ROCK\s+-\s+GREAT COPY!$/i,
    /\s+-\s+REGGAE\s+-\s+GREAT COPY!$/i
  ];

  for (const pattern of suffixPatterns) {
    const match = value.match(pattern);

    if (match) {
      const suffix = match[0];
      const prefix = value.slice(0, -suffix.length);
      const rebuilt = `${prefix} - ${safeFormat}${suffix}`;

      return rebuilt.length <= 80 ? rebuilt : `${prefix} - ${safeFormat}`;
    }
  }

  const rebuilt = `${value} - ${safeFormat}`;
  return rebuilt.length <= 80 ? rebuilt : value;
}

function findTitleField(rootDocument) {
  const selectors = [
    'input[maxlength="80"]',
    'textarea[maxlength="80"]',
    'input[name*="title" i]',
    'textarea[name*="title" i]',
    'input[id*="title" i]',
    'textarea[id*="title" i]',
    'input[aria-label*="title" i]',
    'textarea[aria-label*="title" i]',
    'input[placeholder*="title" i]',
    'textarea[placeholder*="title" i]',
    '[contenteditable="true"][aria-label*="title" i]'
  ];

  const candidates = Array.from(
    rootDocument.querySelectorAll(selectors.join(","))
  )
    .filter(isVisible)
    .map((element) => ({
      element,
      score: scoreTitleField(element)
    }))
    .sort((a, b) => b.score - a.score);

  return candidates[0]?.element || null;
}

function scoreTitleField(element) {
  const searchable = [
    element.id,
    element.getAttribute("name"),
    element.getAttribute("aria-label"),
    element.getAttribute("placeholder"),
    element.getAttribute("data-testid")
  ]
    .filter(Boolean)
    .join(" ")
    .toLowerCase();

  let score = 0;

  if (element.maxLength === 80) {
    score += 10;
  }

  if (/\bitem\s*title\b/.test(searchable)) {
    score += 9;
  } else if (/\btitle\b/.test(searchable)) {
    score += 5;
  }

  if (/search/.test(searchable)) {
    score -= 15;
  }

  if (
    element.tagName === "INPUT" ||
    element.tagName === "TEXTAREA"
  ) {
    score += 2;
  }

  return score;
}

function fillDescriptionAcrossDocuments(release) {
  const documents = [document];

  for (const frame of document.querySelectorAll("iframe")) {
    try {
      if (frame.contentDocument) {
        documents.push(frame.contentDocument);
      }
    } catch (error) {
      console.debug("Could not inspect an eBay iframe:", error);
    }
  }

  let bestResult = {
    foundEditor: false,
    updatedFields: [],
    missingFields: DESCRIPTION_FIELDS.map(
      (field) => field.displayName
    ),
    editor: null
  };

  for (const currentDocument of documents) {
    const editors = findDescriptionEditors(currentDocument);

    for (const editor of editors) {
      const result = fillDescriptionEditor(editor, release);

      if (
        result.updatedFields.length >
        bestResult.updatedFields.length
      ) {
        bestResult = {
          ...result,
          foundEditor: true,
          editor
        };
      }

      if (
        result.updatedFields.length ===
        DESCRIPTION_FIELDS.length
      ) {
        return bestResult;
      }
    }
  }

  return bestResult;
}

function findDescriptionEditors(rootDocument) {
  const selectors = [
    'textarea[name*="description" i]',
    'textarea[id*="description" i]',
    'textarea[aria-label*="description" i]',
    'textarea[placeholder*="description" i]',
    '[contenteditable="true"][aria-label*="description" i]',
    '[contenteditable="true"][data-testid*="description" i]',
    '[role="textbox"][aria-multiline="true"]',
    '[contenteditable="true"]'
  ];

  const candidates = Array.from(
    rootDocument.querySelectorAll(selectors.join(","))
  );

  // Some rich-text editors make the document body itself editable.
  if (
    rootDocument.body &&
    (
      rootDocument.body.isContentEditable ||
      rootDocument.body.getAttribute("contenteditable") === "true"
    )
  ) {
    candidates.unshift(rootDocument.body);
  }

  return [...new Set(candidates)]
    .filter(isVisible)
    .filter((element) => countKnownLabels(element) > 0)
    .sort(
      (a, b) =>
        countKnownLabels(b) - countKnownLabels(a)
    );
}

function countKnownLabels(editor) {
  const text = getEditorText(editor);

  return DESCRIPTION_FIELDS.reduce((count, field) => {
    const found = field.labels.some((label) =>
      buildLabelRegex(label, false).test(text)
    );

    return count + (found ? 1 : 0);
  }, 0);
}

function fillDescriptionEditor(editor, release) {
  if (
    editor instanceof HTMLTextAreaElement ||
    editor instanceof HTMLInputElement
  ) {
    return fillPlainTextEditor(editor, release);
  }

  return fillRichTextEditor(editor, release);
}

function fillPlainTextEditor(editor, release) {
  let text = editor.value;
  const updatedFields = [];
  const missingFields = [];

  for (const field of DESCRIPTION_FIELDS) {
    const rawValue =
      field.key === "format"
        ? release[field.key] || DEFAULT_FORMAT
        : release[field.key];

    const value = cleanText(rawValue);

    if (!value) {
      missingFields.push(field.displayName);
      continue;
    }

    const replacement = replaceLabelledLine(
      text,
      field.labels,
      value
    );

    if (replacement.changed) {
      text = replacement.text;
      updatedFields.push(field.displayName);
    } else {
      missingFields.push(field.displayName);
    }
  }

  if (updatedFields.length) {
    setEditableValue(editor, text);
  }

  return {
    updatedFields,
    missingFields
  };
}

function replaceLabelledLine(text, labels, value) {
  for (const label of labels) {
    const expression = buildLabelRegex(label, true);

    if (expression.test(text)) {
      return {
        changed: true,
        text: text.replace(
          expression,
          (match, prefix) => `${prefix}${value}`
        )
      };
    }
  }

  return {
    changed: false,
    text
  };
}

function fillRichTextEditor(editor, release) {
  const updatedFields = [];
  const missingFields = [];

  for (const field of DESCRIPTION_FIELDS) {
    const rawValue =
      field.key === "format"
        ? release[field.key] || DEFAULT_FORMAT
        : release[field.key];

    const value = cleanText(rawValue);

    if (!value) {
      missingFields.push(field.displayName);
      continue;
    }

    const labelElement = findExactLabelElement(
      editor,
      field.labels
    );

    if (!labelElement) {
      missingFields.push(field.displayName);
      continue;
    }

    const changed = setDescriptionFieldValue(
      editor,
      labelElement,
      field.labels,
      value
    );

    if (changed) {
      updatedFields.push(field.displayName);
    } else {
      missingFields.push(field.displayName);
    }
  }

  if (updatedFields.length) {
    dispatchEditingEvents(editor);
  }

  return {
    updatedFields,
    missingFields
  };
}

function findExactLabelElement(editor, labels) {
  const selector = [
    "p",
    "div",
    "li",
    "td",
    "th",
    "span"
  ].join(",");

  const candidates = Array.from(
    editor.querySelectorAll(selector)
  )
    .filter((element) => {
      const text = getElementText(element);

      if (!text || text.length > 250) {
        return false;
      }

      return labels.some((label) =>
        isExactLabelOrInlineField(text, label)
      );
    })
    .filter((element) => {
      // Reject broad parent containers when a smaller descendant
      // contains the same field label.
      return !Array.from(
        element.querySelectorAll(selector)
      ).some((child) => {
        const childText = getElementText(child);

        return labels.some((label) =>
          isExactLabelOrInlineField(
            childText,
            label
          )
        );
      });
    })
    .sort((a, b) => {
      const aText = getElementText(a);
      const bText = getElementText(b);

      const aLabelOnly = labels.some(
        (label) => isLabelOnly(aText, label)
      );
      const bLabelOnly = labels.some(
        (label) => isLabelOnly(bText, label)
      );

      if (aLabelOnly !== bLabelOnly) {
        return aLabelOnly ? -1 : 1;
      }

      return aText.length - bText.length;
    });

  return candidates[0] || null;
}

function setDescriptionFieldValue(
  editor,
  labelElement,
  labels,
  value
) {
  const labelText = getElementText(labelElement);
  const matchingLabel = labels.find((label) =>
    isExactLabelOrInlineField(
      labelText,
      label
    )
  );

  if (!matchingLabel) {
    return false;
  }

  // Inline template:
  // Label: Columbia
  //
  // Replace only the text after the colon.
  if (!isLabelOnly(labelText, matchingLabel)) {
    return replaceInlineFieldValue(
      labelElement,
      matchingLabel,
      value
    );
  }

  // Blank label template:
  //
  // Artist:
  //
  // Keep the value on the same visual line:
  //
  // Artist: Miles Davis
  //
  // The exact label element has already been selected, so appending
  // here cannot overwrite the neighbouring Title, Format or Label fields.
  const labelLine = getFieldLineContainer(
    editor,
    labelElement
  ) || labelElement;

  // Clean up a value line inserted by version 0.8, if present.
  const nextLine = getNextMeaningfulElement(
    labelLine
  );

  if (
    nextLine &&
    !isKnownFieldLabelElement(nextLine) &&
    isSafeLegacyValueLine(nextLine)
  ) {
    nextLine.remove();
  }

  appendInlineValue(
    labelElement,
    value
  );

  dispatchEditingEvents(labelElement);
  return true;
}

function appendInlineValue(element, value) {
  const ownerDocument =
    element.ownerDocument || document;

  // Remove trailing whitespace so the result is consistently:
  // "Artist: Miles Davis"
  const textNodes = getTextNodes(
    element,
    ownerDocument
  );

  if (textNodes.length) {
    const finalNode =
      textNodes[textNodes.length - 1];

    finalNode.nodeValue =
      String(finalNode.nodeValue || "")
        .replace(/\s+$/, "");
  }

  element.appendChild(
    ownerDocument.createTextNode(` ${value}`)
  );
}

function isSafeLegacyValueLine(element) {
  const text = getElementText(element);

  if (!text || text.length > 250) {
    return false;
  }

  // Never remove a recognised field label.
  if (isKnownFieldLabelElement(element)) {
    return false;
  }

  // Version 0.8 created simple cloned blocks containing only a value.
  // Restrict cleanup to plain short blocks without nested controls,
  // links, images or other meaningful template structure.
  if (
    element.querySelector(
      "input, textarea, select, button, a, img, table, iframe"
    )
  ) {
    return false;
  }

  return ["DIV", "P", "LI", "SPAN"].includes(
    element.tagName
  );
}

function replaceInlineFieldValue(
  element,
  label,
  value
) {
  const ownerDocument =
    element.ownerDocument || document;
  const textNodes = getTextNodes(
    element,
    ownerDocument
  );

  if (!textNodes.length) {
    return false;
  }

  const fullText = textNodes
    .map((node) => node.nodeValue || "")
    .join("");

  const expression = buildSingleLabelRegex(
    label,
    false
  );
  const match = fullText.match(expression);

  if (!match || match.index === undefined) {
    return false;
  }

  const valueStart =
    match.index + match[0].length;

  const startPosition = mapTextOffset(
    textNodes,
    valueStart
  );
  const endPosition = mapTextOffset(
    textNodes,
    fullText.length
  );

  if (!startPosition || !endPosition) {
    return false;
  }

  const range = ownerDocument.createRange();
  range.setStart(
    startPosition.node,
    startPosition.offset
  );
  range.setEnd(
    endPosition.node,
    endPosition.offset
  );
  range.deleteContents();
  range.insertNode(
    ownerDocument.createTextNode(` ${value}`)
  );

  dispatchEditingEvents(element);
  return true;
}

function getFieldLineContainer(
  editor,
  labelElement
) {
  const blockTags = new Set([
    "P",
    "DIV",
    "LI",
    "TD",
    "TH"
  ]);

  let current = labelElement;

  while (
    current &&
    current !== editor
  ) {
    if (
      blockTags.has(current.tagName) &&
      current.parentElement
    ) {
      // Do not climb into a broad block that includes several fields.
      const currentText = getElementText(current);

      if (currentText.length <= 250) {
        return current;
      }
    }

    current = current.parentElement;
  }

  if (labelElement.parentElement === editor) {
    return labelElement;
  }

  return null;
}

function getNextMeaningfulElement(element) {
  let sibling = element.nextElementSibling;

  while (sibling) {
    const text = getElementText(sibling);

    if (text || isKnownFieldLabelElement(sibling)) {
      return sibling;
    }

    sibling = sibling.nextElementSibling;
  }

  return null;
}

function isKnownFieldLabelElement(element) {
  const text = getElementText(element);

  return ALL_DESCRIPTION_FIELDS.some((field) =>
    field.labels.some((label) =>
      isExactLabelOrInlineField(
        text,
        label
      )
    )
  );
}

function isExactLabelOrInlineField(
  text,
  label
) {
  return buildSingleLabelRegex(
    label,
    true
  ).test(cleanLineText(text));
}

function isLabelOnly(text, label) {
  const flexibleLabel = escapeRegExp(label)
    .replace(/\\\./g, "\\.?")
    .replace(/\s+/g, "\\s*");

  return new RegExp(
    `^\\s*${flexibleLabel}\\s*:\\s*$`,
    "i"
  ).test(cleanLineText(text));
}

function buildSingleLabelRegex(
  label,
  allowValue
) {
  const flexibleLabel = escapeRegExp(label)
    .replace(/\\\./g, "\\.?")
    .replace(/\s+/g, "\\s*");

  return new RegExp(
    allowValue
      ? `^\\s*${flexibleLabel}\\s*:\\s*.*$`
      : `^\\s*${flexibleLabel}\\s*:\\s*`,
    "i"
  );
}

function getElementText(element) {
  return cleanLineText(
    element.innerText ||
    element.textContent ||
    ""
  );
}

function buildLabelRegex(label, capturePrefix) {
  const flexibleLabel = escapeRegExp(label)
    // Treat full stops in labels as optional so Cat No, Cat. No and
    // Cat. No. can all match safely.
    .replace(/\\\./g, "\\.?")
    .replace(/\s+/g, "\\s*");

  if (capturePrefix) {
    return new RegExp(
      `^(\\s*${flexibleLabel}\\s*:\\s*).*$`,
      "im"
    );
  }

  return new RegExp(
    `^\\s*${flexibleLabel}\\s*:\\s*`,
    "i"
  );
}

function getTextNodes(root, ownerDocument) {
  const walker = ownerDocument.createTreeWalker(
    root,
    NodeFilter.SHOW_TEXT,
    {
      acceptNode(node) {
        if (!node.nodeValue) {
          return NodeFilter.FILTER_REJECT;
        }

        const parent = node.parentElement;

        if (
          parent &&
          ["SCRIPT", "STYLE", "NOSCRIPT"].includes(parent.tagName)
        ) {
          return NodeFilter.FILTER_REJECT;
        }

        return NodeFilter.FILTER_ACCEPT;
      }
    }
  );

  const nodes = [];
  let current;

  while ((current = walker.nextNode())) {
    nodes.push(current);
  }

  return nodes;
}

function mapTextOffset(nodes, targetOffset) {
  let traversed = 0;

  for (const node of nodes) {
    const length = (node.nodeValue || "").length;

    if (targetOffset <= traversed + length) {
      return {
        node,
        offset: targetOffset - traversed
      };
    }

    traversed += length;
  }

  const finalNode = nodes[nodes.length - 1];

  if (!finalNode) {
    return null;
  }

  return {
    node: finalNode,
    offset: (finalNode.nodeValue || "").length
  };
}

function setEditableValue(element, value) {
  if (element.isContentEditable) {
    element.textContent = value;
    dispatchEditingEvents(element);
    return;
  }

  const prototype =
    element instanceof HTMLTextAreaElement
      ? HTMLTextAreaElement.prototype
      : HTMLInputElement.prototype;

  const descriptor = Object.getOwnPropertyDescriptor(
    prototype,
    "value"
  );

  if (descriptor?.set) {
    descriptor.set.call(element, value);
  } else {
    element.value = value;
  }

  dispatchEditingEvents(element);
}

function dispatchEditingEvents(element) {
  if (
    !element ||
    typeof element.dispatchEvent !== "function"
  ) {
    return;
  }

  element.dispatchEvent(
    new InputEvent("input", {
      bubbles: true,
      inputType: "insertText"
    })
  );

  element.dispatchEvent(
    new Event("change", {
      bubbles: true
    })
  );

  element.dispatchEvent(
    new FocusEvent("blur", {
      bubbles: true
    })
  );
}

function scrollToDescriptionEditor(editor) {
  if (!editor) {
    return;
  }

  editor.scrollIntoView({
    behavior: "smooth",
    block: "center"
  });
}

function getEditorText(editor) {
  if (
    editor instanceof HTMLTextAreaElement ||
    editor instanceof HTMLInputElement
  ) {
    return editor.value;
  }

  return editor.innerText || editor.textContent || "";
}

function isVisible(element) {
  const view =
    element.ownerDocument?.defaultView || window;
  const style = view.getComputedStyle(element);
  const rectangle = element.getBoundingClientRect();

  return (
    style.display !== "none" &&
    style.visibility !== "hidden" &&
    rectangle.width > 0 &&
    rectangle.height > 0
  );
}

function cleanLineText(value) {
  return String(value || "")
    .replace(/\u00a0/g, " ")
    .replace(/[ \t]+/g, " ")
    .trim();
}

function cleanText(value) {
  return String(value || "")
    .replace(/\u00a0/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function escapeRegExp(value) {
  return String(value).replace(
    /[.*+?^${}()|[\]\\]/g,
    "\\$&"
  );
}


