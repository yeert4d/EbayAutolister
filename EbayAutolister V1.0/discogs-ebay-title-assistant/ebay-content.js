async function applyConditionAssistantState(
  state
) {
  const recordCondition =
    buildConditionValue(
      state.vinyl,
      false
    );

  const sleeveCondition =
    buildConditionValue(
      state.sleeve,
      true
    );

  // The condition-description / Seller Notes field is
  // deliberately updated first.
  const conditionField =
    findConditionDescriptionFieldAcrossDocuments();

  if (!conditionField) {
    throw new Error(
      "Could not find the Seller Notes or " +
      "Condition Description field."
    );
  }

  fillSellerNotesField(
    conditionField,
    recordCondition,
    sleeveCondition
  );

  // Only after Seller Notes has been updated are the same
  // values copied into the existing item-description fields.
  const descriptionResult =
    fillConditionFieldsAcrossDocuments({
      recordCondition,
      sleeveCondition
    });

  return {
    recordCondition,
    sleeveCondition,
    descriptionResult
  };
}

function getAccessibleDocuments() {
  const documents = [];
  const visited = new Set();

  function collect(currentDocument) {
    if (
      !currentDocument ||
      visited.has(currentDocument)
    ) {
      return;
    }

    visited.add(currentDocument);
    documents.push(currentDocument);

    for (
      const frame of
      currentDocument.querySelectorAll(
        "iframe"
      )
    ) {
      try {
        if (frame.contentDocument) {
          collect(frame.contentDocument);
        }
      } catch (error) {
        console.debug(
          "Could not inspect an eBay iframe:",
          error
        );
      }
    }
  }

  collect(document);
  return documents;
}

function findConditionDescriptionFieldAcrossDocuments() {
  const candidates = [];

  for (
    const currentDocument of
    getAccessibleDocuments()
  ) {
    const selectors = [
      "textarea",
      'input[type="text"]',
      'input:not([type])',
      '[contenteditable="true"]',
      '[role="textbox"]'
    ];

    for (
      const element of
      currentDocument.querySelectorAll(
        selectors.join(",")
      )
    ) {
      if (!isVisible(element)) {
        continue;
      }

      const score =
        scoreConditionDescriptionField(
          element,
          currentDocument
        );

      if (score > 0) {
        candidates.push({
          element,
          score
        });
      }
    }
  }

  candidates.sort(
    (a, b) => b.score - a.score
  );

  return candidates[0]?.element || null;
}

function scoreConditionDescriptionField(
  element,
  ownerDocument
) {
  const attributes = [
    element.id,
    element.getAttribute("name"),
    element.getAttribute("aria-label"),
    element.getAttribute("placeholder"),
    element.getAttribute("data-testid"),
    element.getAttribute("title")
  ]
    .filter(Boolean)
    .join(" ")
    .toLowerCase();

  const value =
    getEditableFieldText(element)
      .toLowerCase();

  const associatedText =
    getAssociatedFieldText(
      element,
      ownerDocument
    ).toLowerCase();

  const searchable =
    `${attributes} ${associatedText}`;

  let score = 0;

  if (
    /vinyl\s*grade\s*:/.test(value)
  ) {
    score += 120;
  }

  if (
    /sleeve\s*grade\s*:/.test(value)
  ) {
    score += 120;
  }

  if (/seller\s*notes?/.test(searchable)) {
    score += 100;
  }

  if (
    /condition\s*description/.test(
      searchable
    )
  ) {
    score += 95;
  }

  if (
    /condition\s*(details?|notes?)/.test(
      searchable
    )
  ) {
    score += 70;
  }

  if (
    /item\s*description/.test(searchable) &&
    !/condition/.test(searchable)
  ) {
    score -= 120;
  }

  if (
    /\btitle\b/.test(searchable) &&
    !/condition/.test(searchable)
  ) {
    score -= 80;
  }

  return score;
}

function getAssociatedFieldText(
  element,
  ownerDocument
) {
  const values = [];

  if (element.id) {
    const safeId =
      typeof CSS !== "undefined" &&
      typeof CSS.escape === "function"
        ? CSS.escape(element.id)
        : element.id.replace(
            /["\\]/g,
            "\\$&"
          );

    const labels =
      ownerDocument.querySelectorAll(
        `label[for="${safeId}"]`
      );

    for (const label of labels) {
      values.push(
        label.innerText ||
        label.textContent ||
        ""
      );
    }
  }

  const closestLabel =
    element.closest("label");

  if (closestLabel) {
    values.push(
      closestLabel.innerText ||
      closestLabel.textContent ||
      ""
    );
  }

  const parent = element.parentElement;

  if (parent) {
    values.push(
      String(
        parent.innerText ||
        parent.textContent ||
        ""
      ).slice(0, 500)
    );
  }

  return values.join(" ");
}

function getEditableFieldText(element) {
  if (
    element instanceof HTMLInputElement ||
    element instanceof HTMLTextAreaElement
  ) {
    return element.value || "";
  }

  return (
    element.innerText ||
    element.textContent ||
    ""
  );
}

function fillSellerNotesField(
  field,
  recordCondition,
  sleeveCondition
) {
  const existingText =
    getEditableFieldText(field);

  let updatedText = upsertConditionLine(
    existingText,
    [
      "Vinyl Grade"
    ],
    "Vinyl Grade",
    recordCondition
  );

  updatedText = upsertConditionLine(
    updatedText,
    [
      "Sleeve Grade"
    ],
    "Sleeve Grade",
    sleeveCondition
  );

  const maxLength =
    Number(field.maxLength);

  if (
    Number.isFinite(maxLength) &&
    maxLength > 0 &&
    updatedText.length > maxLength
  ) {
    throw new Error(
      "The generated condition description " +
      `is ${updatedText.length} characters, ` +
      `but the field limit is ${maxLength}.`
    );
  }

  setEditableValue(
    field,
    updatedText
  );
}

function upsertConditionLine(
  text,
  labels,
  canonicalLabel,
  value
) {
  const source = String(text || "");
  const alternatives = labels
    .map((label) =>
      escapeRegExp(label)
        .replace(/\s+/g, "\\s*")
    )
    .join("|");

  const expression = new RegExp(
    `^(\\s*(?:${alternatives})\\s*:\\s*).*$`,
    "im"
  );

  const sentence =
    `${cleanConditionNotes(value)}.`;

  if (expression.test(source)) {
    return source.replace(
      expression,
      (match, prefix) =>
        `${prefix}${sentence}`
    );
  }

  const separator =
    source.trim()
      ? source.endsWith("\n")
        ? ""
        : "\n"
      : "";

  return (
    `${source}${separator}` +
    `${canonicalLabel}: ${sentence}`
  );
}

function fillConditionFieldsAcrossDocuments(
  values
) {
  let bestResult = {
    foundEditor: false,
    updatedFields: [],
    missingFields:
      CONDITION_DESCRIPTION_FIELDS.map(
        (field) => field.displayName
      ),
    editor: null
  };

  for (
    const currentDocument of
    getAccessibleDocuments()
  ) {
    const editors =
      findDescriptionEditors(
        currentDocument
      );

    for (const editor of editors) {
      const result =
        fillConditionFieldsInEditor(
          editor,
          values
        );

      if (
        result.updatedFields.length >
        bestResult.updatedFields.length
      ) {
        bestResult = {
          ...result,
          foundEditor: true,
          editor
        };
      }

      if (
        result.updatedFields.length ===
        CONDITION_DESCRIPTION_FIELDS.length
      ) {
        return bestResult;
      }
    }
  }

  return bestResult;
}

function fillConditionFieldsInEditor(
  editor,
  values
) {
  if (
    editor instanceof HTMLInputElement ||
    editor instanceof HTMLTextAreaElement
  ) {
    return fillPlainConditionFields(
      editor,
      values
    );
  }

  return fillRichConditionFields(
    editor,
    values
  );
}

function fillPlainConditionFields(
  editor,
  values
) {
  let text = editor.value;
  const updatedFields = [];
  const missingFields = [];

  for (
    const field of
    CONDITION_DESCRIPTION_FIELDS
  ) {
    const value = cleanText(
      values[field.key]
    );

    if (!value) {
      missingFields.push(
        field.displayName
      );
      continue;
    }

    const replacement =
      replaceLabelledLine(
        text,
        field.labels,
        value
      );

    if (replacement.changed) {
      text = replacement.text;
      updatedFields.push(
        field.displayName
      );
    } else {
      missingFields.push(
        field.displayName
      );
    }
  }

  if (updatedFields.length) {
    setEditableValue(editor, text);
  }

  return {
    updatedFields,
    missingFields
  };
}

function fillRichConditionFields(
  editor,
  values
) {
  const updatedFields = [];
  const missingFields = [];

  for (
    const field of
    CONDITION_DESCRIPTION_FIELDS
  ) {
    const value = cleanText(
      values[field.key]
    );

    const labelElement =
      findExactLabelElement(
        editor,
        field.labels
      );

    if (!value || !labelElement) {
      missingFields.push(
        field.displayName
      );
      continue;
    }

    const changed =
      setDescriptionFieldValue(
        editor,
        labelElement,
        field.labels,
        value
      );

    if (changed) {
      updatedFields.push(
        field.displayName
      );
    } else {
      missingFields.push(
        field.displayName
      );
    }
  }

  if (updatedFields.length) {
    dispatchEditingEvents(editor);
  }

  return {
    updatedFields,
    missingFields
  };
}

function showToast(message, type) {
  document.getElementById("discogs-ebay-title-toast")?.remove();

  const toast = document.createElement("div");
  toast.id = "discogs-ebay-title-toast";
  toast.textContent = message;

  const backgrounds = {
    success: "#173d2b",
    warning: "#665200",
    error: "#5a1f1f"
  };

  Object.assign(toast.style, {
    position: "fixed",
    top: "20px",
    right: "20px",
    zIndex: "2147483647",
    maxWidth: "620px",
    padding: "14px 18px",
    borderRadius: "8px",
    background: backgrounds[type] || backgrounds.error,
    color: "#ffffff",
    fontFamily: "Arial, sans-serif",
    fontSize: "14px",
    lineHeight: "1.4",
    boxShadow: "0 6px 24px rgba(0, 0, 0, 0.3)"
  });

  document.body.appendChild(toast);

  window.setTimeout(() => {
    toast.remove();
  }, type === "error" ? 8500 : 6500);
}

