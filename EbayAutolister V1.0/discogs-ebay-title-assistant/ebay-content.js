async function initialiseConditionAssistantControls(
  shadow
) {
  const stored = await browser.storage.local.get(
    CONDITION_ASSISTANT_STORAGE_KEY
  );

  const state = normaliseConditionAssistantState(
    stored[CONDITION_ASSISTANT_STORAGE_KEY]
  );

  const controls = {
    body: shadow.getElementById("condition-body"),
    collapse:
      shadow.getElementById("condition-collapse"),
    apply:
      shadow.getElementById("condition-apply"),
    status:
      shadow.getElementById("condition-status"),
    vinyl: {
      grade:
        shadow.getElementById("vinyl-grade"),
      notes:
        shadow.getElementById("vinyl-notes"),
      minus:
        shadow.getElementById("vinyl-minus"),
      plus:
        shadow.getElementById("vinyl-plus"),
      plusCount:
        shadow.getElementById(
          "vinyl-plus-count"
        ),
      preview:
        shadow.getElementById(
          "vinyl-preview"
        )
    },
    sleeve: {
      grade:
        shadow.getElementById("sleeve-grade"),
      notes:
        shadow.getElementById("sleeve-notes"),
      minus:
        shadow.getElementById("sleeve-minus"),
      plus:
        shadow.getElementById("sleeve-plus"),
      plusCount:
        shadow.getElementById(
          "sleeve-plus-count"
        ),
      preview:
        shadow.getElementById(
          "sleeve-preview"
        )
    }
  };

  controls.vinyl.grade.value =
    state.vinyl.grade;
  controls.vinyl.notes.value =
    state.vinyl.notes;
  controls.sleeve.grade.value =
    state.sleeve.grade;
  controls.sleeve.notes.value =
    state.sleeve.notes;

  controls.body.hidden = state.collapsed;
  controls.collapse.textContent =
    state.collapsed ? "+" : "−";

  bindConditionSectionControls(
    "vinyl",
    state,
    controls
  );
  bindConditionSectionControls(
    "sleeve",
    state,
    controls
  );

  controls.collapse.addEventListener(
    "click",
    async () => {
      state.collapsed = !state.collapsed;
      controls.body.hidden =
        state.collapsed;
      controls.collapse.textContent =
        state.collapsed ? "+" : "−";

      await saveConditionAssistantState(
        state
      );
    }
  );

  controls.apply.addEventListener(
    "click",
    async () => {
      controls.apply.disabled = true;
      setConditionPanelStatus(
        controls.status,
        "Applying condition...",
        ""
      );

      try {
        await saveConditionAssistantState(
          state
        );

        const result =
          await applyConditionAssistantState(
            state
          );

        const missingDescriptionFields =
          result.descriptionResult
            .missingFields || [];

        let message =
          "Seller Notes and item-description " +
          "condition fields were updated.";

        if (
          missingDescriptionFields.length
        ) {
          message +=
            " Missing description fields: " +
            missingDescriptionFields.join(
              ", "
            ) +
            ".";
        }

        setConditionPanelStatus(
          controls.status,
          message,
          missingDescriptionFields.length
            ? ""
            : "success"
        );

        showToast(
          message +
            " Review the listing before saving.",
          missingDescriptionFields.length
            ? "warning"
            : "success"
        );
      } catch (error) {
        const message =
          error.message ||
          "Unable to apply the condition.";

        setConditionPanelStatus(
          controls.status,
          message,
          "error"
        );
        showToast(message, "error");
        console.error(error);
      } finally {
        controls.apply.disabled = false;
      }
    }
  );

  renderConditionAssistant(
    state,
    controls
  );
}

function bindConditionSectionControls(
  sectionName,
  state,
  controls
) {
  const section =
    controls[sectionName];

  section.grade.addEventListener(
    "change",
    async () => {
      state[sectionName].grade =
        section.grade.value;

      renderConditionAssistant(
        state,
        controls
      );
      await saveConditionAssistantState(
        state
      );
    }
  );

  section.notes.addEventListener(
    "input",
    () => {
      state[sectionName].notes =
        section.notes.value;

      renderConditionAssistant(
        state,
        controls
      );
      queueConditionStateSave(state);
    }
  );

  section.plus.addEventListener(
    "click",
    async () => {
      state[sectionName].pluses =
        Math.min(
          MAX_GRADE_PLUSES,
          state[sectionName].pluses + 1
        );

      renderConditionAssistant(
        state,
        controls
      );
      await saveConditionAssistantState(
        state
      );
    }
  );

  section.minus.addEventListener(
    "click",
    async () => {
      state[sectionName].pluses =
        Math.max(
          0,
          state[sectionName].pluses - 1
        );

      renderConditionAssistant(
        state,
        controls
      );
      await saveConditionAssistantState(
        state
      );
    }
  );
}

let conditionStateSaveTimer = null;

function queueConditionStateSave(state) {
  window.clearTimeout(
    conditionStateSaveTimer
  );

  conditionStateSaveTimer =
    window.setTimeout(() => {
      saveConditionAssistantState(state);
    }, 250);
}

async function saveConditionAssistantState(
  state
) {
  await browser.storage.local.set({
    [CONDITION_ASSISTANT_STORAGE_KEY]:
      normaliseConditionAssistantState(state)
  });
}

function normaliseConditionAssistantState(
  candidate
) {
  const source =
    candidate &&
    typeof candidate === "object"
      ? candidate
      : {};

  return {
    vinyl: normaliseConditionSection(
      source.vinyl,
      DEFAULT_CONDITION_ASSISTANT_STATE
        .vinyl
    ),
    sleeve: normaliseConditionSection(
      source.sleeve,
      DEFAULT_CONDITION_ASSISTANT_STATE
        .sleeve
    ),
    collapsed:
      typeof source.collapsed === "boolean"
        ? source.collapsed
        : false
  };
}

function normaliseConditionSection(
  section,
  fallback
) {
  const validGrades = new Set([
    "Good",
    "Very Good",
    "Excellent"
  ]);

  const source =
    section &&
    typeof section === "object"
      ? section
      : {};

  const grade = validGrades.has(
    source.grade
  )
    ? source.grade
    : fallback.grade;

  const parsedPluses = Number(
    source.pluses
  );

  const pluses = Number.isFinite(
    parsedPluses
  )
    ? Math.max(
        0,
        Math.min(
          MAX_GRADE_PLUSES,
          Math.trunc(parsedPluses)
        )
      )
    : fallback.pluses;

  return {
    grade,
    pluses,
    notes:
      typeof source.notes === "string"
        ? source.notes
        : fallback.notes
  };
}

function renderConditionAssistant(
  state,
  controls
) {
  for (const sectionName of [
    "vinyl",
    "sleeve"
  ]) {
    const sectionState =
      state[sectionName];
    const sectionControls =
      controls[sectionName];

    sectionControls.plusCount.textContent =
      `${sectionState.pluses}/4`;

    sectionControls.preview.textContent =
      buildConditionValue(
        sectionState,
        sectionName === "sleeve"
      );
  }
}

function formatConditionGrade(
  grade,
  pluses
) {
  const safePluses = Math.max(
    0,
    Math.min(
      MAX_GRADE_PLUSES,
      Math.trunc(Number(pluses) || 0)
    )
  );

  const plusText =
    "+".repeat(safePluses);

  if (
    grade === "Excellent" &&
    safePluses === MAX_GRADE_PLUSES
  ) {
    return (
      `Excellent${plusText} ` +
      "to Near Mint"
    );
  }

  return `${grade}${plusText}`;
}

function buildConditionValue(
  section,
  isSleeve
) {
  const grade = formatConditionGrade(
    section.grade,
    section.pluses
  );

  const notes = cleanConditionNotes(
    section.notes
  );

  let value = notes
    ? `${grade}, ${notes}`
    : grade;

  if (
    isSleeve &&
    !/\(see associated images\)$/i.test(
      value
    )
  ) {
    value += " (see associated images)";
  }

  return value;
}

function cleanConditionNotes(value) {
  return String(value || "")
    .replace(/\u00a0/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/[.\s]+$/g, "");
}

